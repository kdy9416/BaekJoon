package Level_1;

import java.util.Scanner;

public class Number9 {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		int a,b;
		a=scan.nextInt();
		b=scan.nextInt();
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		int c =a/b;
		System.out.println(c);
		System.out.println(a%b);

	}

}
